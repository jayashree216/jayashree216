package day1;

public class ConditionalOperators {
    public static void main(String[] args) {

        int x=10;
        int y=20;
        System.out.println(y==x && x<y);
        System.out.println(y==x || x<y);
        System.out.println(y==x);
        System.out.println(y!=x);
        System.out.println(y<x);
        System.out.println(y>x);
        System.out.println(y>=x);
        System.out.println(y<=x);
        System.out.println(x>=y);
        System.out.println(x<=y);


        String ln ="java";
        String fn="project";

        System.out.println(ln+fn);//concatination



    }

}
